# Function - Train Neural Network Model for predicting stocks

def train_neural_network(stock, data):

    X_test = data['X_test']
    X_train = data['X_train']
    y_test = data['y_test']
    y_train = data['y_train']


    # Neural Network Parameters
    feature_inputs = X_train.shape[1]
    hidden_layer1 = 64 # 1st layer
    hidden_layer2= 32 # 2nd layer

    # Build the Neural Network Model
    model = Sequential() # Initalise the sequential model

    model.add(Input(shape=(feature_inputs,))) # add input layer
    model.add(Dense(hidden_layer1, activation='relu')) # add first hidden layer
    model.add(Dropout(0.2)) # drops 20% layer
    model.add(Dense(hidden_layer2, activation='relu')) # add 2nd hidden layer
    model.add(Dense(1)) # add output layer

    # Compile model
    model.compile(loss='mean_squared_error', optimizer='adam', metrics=['mae']) # adam optimiser to adjust the learning rates for improved performance

    print(model.summary())

    # Define the training parameters
    batch_size = 32 # sample size
    epochs = 50 # Complete loops through the training data

    # Train the model on the training data set
    # store the losses and evaluation metrics
    history = model.fit(
        X_train, y_train,
        batch_size=batch_size,
        epochs=epochs,
        validation_split=0.2,
        verbose=1
    )

    # Make the predictions
    y_train_pred = model.predict(X_train)
    y_test_pred = model.predict(X_test)

    # Model Evaluation Metrics
    mse = mean_squared_error(y_test, y_test_pred.flatten())
    mae = mean_absolute_error(y_test, y_test_pred.flatten())
    r2 = r2_score(y_test, y_test_pred.flatten())

