# Function: Calculate the Moving Averages (MA)

def moving_average(series, period):
    return series.rolling(window=period).mean() # Return a panda series of MA's

# Function: Calculate the Relative Strenght Index (RSI)
def relative_strength_index(series, period):
    delta = series.diff()  # Calculate the difference in current and previous prices
    gain = delta.where(delta > 0, 0)  # keep only gains
    loss = -delta.where(delta < 0, 0)  # keep only losses (as positive values)

    # Calculate the average gain and loss
    avg_gain = gain.rolling(window=period, min_periods=1).mean()
    avg_loss = loss.rolling(window=period, min_periods=1).mean()

    # Compute RS & RSI
    RS = avg_gain / avg_loss # Calcualte relative strenght
    RSI = 100 - (100/(1+RS)) # Calculate RSI
    return RSI

# Function: Process the stock data into a dataframe

def stock_data_processing(stock, stocks_data, market_data):
    # Initialise the stock DataFrame
    df_stock = pd.DataFrame()

    # Input the dataset
    df_stock['high'] = stocks_data['High'][stock]  # Daily high prices
    df_stock['low'] = stocks_data['Low'][stock]    # Daily low price
    df_stock['volume'] = stocks_data['Volume'][stock]  # Trading volume
    df_stock['close'] = stocks_data['Close'][stock]  # Closing prices
    df_stock['open'] = stocks_data['Open'][stock]  # Opening prices

    # Add the market data
    df_stock['market_close'] = market_data['Close']  # Market closing prices
    df_stock['market_volume'] = market_data['Volume']  # Market trading volume

    # Calculate the stocks and market daily returns
    df_stock['return'] = df_stock['close'].pct_change()
    df_stock['market_return'] = df_stock['market_close'].pct_change()

    return df_stock

"""# Feature Engineering"""

# Function: for feature Engineering

def feature_engineering(stock, df_stock):
    # Create the lag feaures of the closing prices and returns
    for i in range(1, 7): # Create a 6 day lag for each stock
        df_stock[f'close_lag{i}'] = df_stock['close'].shift(i)
        df_stock[f'return_lag{i}'] = df_stock['return'].shift(i)

    # Calculate the MA and RSI features
    for n in [10, 50, 200]: # 10, 15, 200 day moving averages
        df_stock[f'ma_{n}'] = moving_average(df_stock['close'], n) / df_stock['close'] # calculate MA of n days - closing price to normalise
        df_stock[f'rsi_{n}'] = relative_strength_index(df_stock['close'], n) # calculate RSI of n days

    # Calculate the volatility of the returns
    for n in [5, 10, 20]:
        df_stock[f'volatility_{n}'] = df_stock['return'].rolling(window=n).std() # calculate the standard deviation of the returns

    return df_stock

