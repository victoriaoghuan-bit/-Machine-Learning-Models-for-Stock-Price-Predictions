# Function: to clean the dataset
def clean_data(stock, df_stock):
    # Remove any missing values
    df_stock.dropna(inplace=True)

    # Remove any outliers
    for col in ['return', 'volume']: # Check for outliers in the return and volune columns
        Q1 = df_stock[col].quantile(0.25)
        Q3 = df_stock[col].quantile(0.75)
        IQR = Q3 - Q1

        # Filter Tthe outliers -Keep values greater or equaL to Q1 and less than or equal to Q3
        df_stock = df_stock[(df_stock[col] >= Q1 - 1.5 * IQR) & (df_stock[col] <= Q3 + 1.5 * IQR)]

    #  Normalise the features except the returns
    features = df_stock.columns.drop('return') # Choose all the features but drop return
    df_norm_stock= df_stock.copy() # copy of normalised features

    # Loop through all the feature columns
    for feature in features:
        std = df_stock[feature].std() # Standard deviation
        mean = df_stock[feature].mean() # Mean

     # Standardise the features
        df_norm_stock[feature] = (df_stock[feature] - mean) / std

    return df_stock, df_norm_stock

